-- MODIFY :
ALTER TABLE EMPL1 MODIFY SALARY DECIMAL(10,2);

-- LOGICAL OPERATOR :
SELECT DEPARTMENT , EMP_ID, NAME FROM EMPL1 WHERE DEPARTMENT='HR' OR DEPARTMENT='SALES';
SELECT DEPARTMENT , EMP_ID , NAME FROM EMPL1 WHERE DEPARTMENT='HR' AND DATE=28/11/2025;
SELECT *FROM EMPL1 WHERE DEPARTMENT IN('HR' , 'SALES');

-- SUM :
SELECT DEPARTMENT, SUM(SALARY) FROM EMPL1 GROUP BY DEPARTMENT;

-- MAX AND MIN :
SELECT DEPARTMENT , MAX(SALARY) , MIN(SALARY) FROM EMPL1 GROUP BY DEPARTMENT;

-- AVG SALARY :
SELECT DEPARTMENT , AVG(SALARY) FROM EMPL1 GROUP BY DEPARTMENT ORDER BY AVG(SALARY) DESC;

-- COUNT :
SELECT DEPARTMENT , COUNT(EMP_ID) FROM EMP1 GROUP BY DEPARTMENT;
SELECT DEPARTMENT , SALARY, COUNT(EMP_ID), AVG(SALARY) FROM EMPL1 GROUP BY DEPARTMENT HAVING AVG(SALARY)>SALARY;

-- 2ND HIGHEST SALARY :
SELECT * FROM EMPL1 ORDER BY SALARY DESC LIMIT 1 OFFSET 1;

