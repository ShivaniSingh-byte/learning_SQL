-- EMPLOYEES TABLE :
CREATE TABLE EMPL1(EMP_ID INT PRIMARY KEY , NAME VARCHAR(20) NOT NULL, DEPARTMENT VARCHAR(20) DEFAULT'SALES', SALARY INT , DATE DATE);

SELECT * FROM EMPL1;

